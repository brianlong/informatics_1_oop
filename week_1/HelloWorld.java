public class HelloWorld {
  public static void main(String[] args) {
    System.out.println("Hello World");
  }
}
// public        - Denotes that the method is visible to every part of the
//                 program.
// static        - Denotes that there is only one of this method.
// void          - Specifies the return type of the method. As this is the
//                 method for our entry-point into the program, this must be
//                 void as it does not return anything.
// main          - This is the name of the method is the entry-point into our
//                 program.
// String[] args - Inside the parentheses we get the parameter of the method;
//                 this indicates any arguments that are passed in from the
//                 command line.
